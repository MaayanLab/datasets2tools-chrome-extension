var Page = {

	/////////////////////////////////
	////// 3.1.1 isGEO
	/////////////////////////////////

	isGEO: function() {
		return Boolean(window.location.hostname === 'www.ncbi.nlm.nih.gov');
	},

	/////////////////////////////////
	////// 3.1.2 isDataMed
	/////////////////////////////////

	isDataMed: function() {
		return Boolean(window.location.hostname === 'datamed.org');
	},

	/////////////////////////////////
	////// 3.1.3 isLDP
	/////////////////////////////////

	isLDP: function() {
		return Boolean(window.location.hostname === 'lincsportal.ccs.miami.edu');
	},
	
	/////////////////////////////////
	////// 3.1.4 getLabel
	/////////////////////////////////

	getLabel: function() {
		var self = this;

		if (self.isGEO()) {
			return 'geo';
		} else if (self.isDataMed()) {
			return 'datamed';
		}
	}
};